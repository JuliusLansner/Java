package ForLoop;

public class Main {
    public static void main(String[] args){
        //for loop executes a block of code for a limited time, based on the variable: i.
        // i=index.
        // this program counts from 10-0
        for(int i = 10; i>=0; i--){
            System.out.println(i);
        }
        System.out.println("happy new year XD");
        // this code counts from 0-10
        for(int i = 0; i<=10; i++){
            System.out.println(i);
        }

    }
}
